	public static String getSignatureAlgorithmForXMSSParameter(String xmss_parameter) {
		return "XMSS-" + extractTypeAndLength(xmss_parameter);
	}

	public static String getSignatureAlgorithmForXMSSMTParameter(String xmssmt_parameter) {
		return "XMSSMT-" + extractTypeAndLength(xmssmt_parameter.replaceFirst("XMSSMT_", ""));
	}

	private static String extractTypeAndLength(String parameter) {
		if (parameter.startsWith("SHA2_") && parameter.endsWith("_256"))
			return "SHA256";
		if (parameter.startsWith("SHA2_") && parameter.endsWith("_512"))
			return "SHA512";
		if (parameter.startsWith("SHAKE_") && parameter.endsWith("_256"))
			return "SHAKE128";
		if (parameter.startsWith("SHAKE_") && parameter.endsWith("_512"))
			return "SHAKE256";
		if (parameter.startsWith("SHAKE256_") && parameter.endsWith("_256"))
			return "SHAKE256_256";
		if (parameter.startsWith("SHAKE256_") && parameter.endsWith("_192"))
			return "SHAKE256_192";
		if (parameter.startsWith("SHA2_") && parameter.endsWith("_192"))
			return "SHA2_192";
		else
			throw new IllegalArgumentException("Unknown signature type for parameter");
	}

